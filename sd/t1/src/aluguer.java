public class aluguer {
    private int veiculoID;
    private int clienteID;
    private float valor;
    private int dataInicio;
    private int duracaoPrev;
    private int aluguerID;
    public int getVeiculoID() {
        return veiculoID;
    }
    public void setVeiculoID(int veiculoID) {
        this.veiculoID = veiculoID;
    }
    public int getClienteID() {
        return clienteID;
    }
    public void setClienteID(int clienteID) {
        this.clienteID = clienteID;
    }
    public float getValor() {
        return valor;
    }
    public void setValor(float valor) {
        this.valor = valor;
    }
    public int getDataInicio() {
        return dataInicio;
    }
    public void setDataInicio(int dataInicio) {
        this.dataInicio = dataInicio;
    }
    public int getDuracaoPrev() {
        return duracaoPrev;
    }
    public void setDuracaoPrev(int duracaoPrev) {
        this.duracaoPrev = duracaoPrev;
    }
    public int getAluguerID() {
        return aluguerID;
    }
    public void setAluguerID(int aluguerID) {
        this.aluguerID = aluguerID;
    }

}
